package messaging.system.domain;

public class Notification {

	private String content;
	
	public Notification() {
	}

	public Notification(String content) {
		super();
		this.content = content;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}
	
	
	
}
